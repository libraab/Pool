void	ft_rev_int_tab(int *tab, int size)
{
	int x;
	int y;
	int temp;

	x = 0;
	y = size - 1;
	temp = 0;
	while (x <= size / 2)
	{
		tab[x] = temp;
		tab[x] = tab[y];
		tab[y] = temp;
		x++;
		y--;
	}
}
