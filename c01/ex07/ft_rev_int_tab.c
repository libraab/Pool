void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int b;
	int temp;

	i = 0;
	b = size - 1;
	temp = 0;
	while (i <= size / 2)
	{
		tab[i] = temp;
		tab[i] = tab[b];
		tab[b] = temp;
		i++;
		b--;
	}
}
